	<p>Yii is an open source project released under the terms of the <a href="/license/">BSD License</a>.
		This means that you can use Yii for free to develop either open-source or proprietary Web applications.</p>

	<p>Currently there are two major versions of Yii: <a href="#yii2">2.0</a> and <a href="#yii1">1.1</a>.</p>


    <h2 id="yii2">Yii 2.0</h2>

    <p>The latest version of Yii 2 is <b>2.0.8</b>, released on <b>April 28, 2016</b>. Yii 2.0 is a complete
        rewrite of Yii on top of PHP 5.4.0. It is aimed to be a state-of-the-art of
        the new generation of PHP framework. Yii 2.0 is not compatible with 1.1.</p>

    <ul>
        <li><a href="https://github.com/yiisoft/yii2/blob/2.0.8/framework/CHANGELOG.md">complete change log</a></li>
        <li><a href="https://github.com/yiisoft/yii2/blob/2.0.8/framework/UPGRADE.md">upgrade instructions</a></li>
    </ul>
    <p>There are two ways to install Yii 2: using Composer, or downloading an application template. We highly recommend
       you to use the former.</p>

    <h3>Install via Composer</h3>

    <p>This is the preferred way of installing Yii 2.0. If you do not have Composer yet, you may install it by
        following the instructions <a href="http://getcomposer.org/doc/00-intro.md#installation-nix" rel="nofollow">here</a>.</p>

    <p>After installing Composer, run the following command to install the <a href="https://github.com/francoispluchino/composer-asset-plugin">Composer Asset Plugin</a>:</p>
    <pre class="instructions">php composer.phar global require "fxp/composer-asset-plugin:~1.1.1"</pre>

    <p>Now choose one of the application templates to start installing Yii 2.0. An application template is a package that
       contains a skeleton Web application written in Yii.</p>
    <ul>
        <li>To install the basic application template, run the command below:
            <pre class="instructions">php composer.phar create-project yiisoft/yii2-app-basic basic 2.0.8</pre>
        </li>
        <li>To install the advanced application template, run the command below:
            <pre class="instructions">php composer.phar create-project yiisoft/yii2-app-advanced advanced 2.0.8</pre>
        </li>
    </ul>
    <p>Note that you may be prompted to enter your GitHub username and password during the installation process. This is normal. Just enter them and continue.</p>

    <h3>Install from an Archive File</h3>

    <p>Download one of the following archive files, and then extract it to a Web-accessible folder:
    </p>
    <ul>
        <li><a href="https://github.com/yiisoft/yii2/releases/download/2.0.8/yii-basic-app-2.0.8.tgz">Yii 2 with basic application template</a></li>
        <li><a href="https://github.com/yiisoft/yii2/releases/download/2.0.8/yii-advanced-app-2.0.8.tgz">Yii 2 with advanced application template</a></li>
    </ul>

    <h3>Upgrade from Older Versions</h3>
    <p>
        If you are upgrading from Yii 2.0.0 with composer, simply run the following commands in your project's root directory:
    </p>
    <p>First, make sure you have the latest version of the <a href="https://github.com/francoispluchino/composer-asset-plugin">Composer Asset Plugin</a>:</p>
    <pre class="instructions">php composer.phar global require "fxp/composer-asset-plugin:~1.1.1"</pre>
    <p>Then upgrade Yii by running:</p>
    <pre class="instructions">php composer.phar update yiisoft/yii2 yiisoft/yii2-composer bower-asset/jquery.inputmask</pre>
    <p>
        If you installed Yii using an archive file, you should download the new release file, extract it and replace the older version with the new one.
    </p>
    <p>
        Upgrading from Yii 1.1 is not trivial. Please refer to the guide about <a href="http://www.yiiframework.com/doc-2.0/guide-intro-upgrade-from-v1.html">upgrading from Yii 1.1</a>.
    </p>

    <h3>Obtain the Latest Code</h3>

    <p>Yii 2.0 is hosted on <a href="https://github.com/yiisoft/yii2">GitHub</a>. You are welcome to contribute
        to the Yii 2.0 development by submitting <a href="https://github.com/yiisoft/yii2/issues?state=open">issue reports</a>
        or <a href="https://github.com/yiisoft/yii2/pulls">merge requests</a>.</p>


